package If_For;

public class b_score {
    public  static  void main(String[] args){
        int score =95;

        if(score>=90){
            System.out.println("A+입니다.");
        }else if(score>=80){
            System.out.println("B+입니다.");
        }else if(score>=70){
            System.out.println("B입니다.");
        }else if(score>=60){
            System.out.println("C+입니다.");
        }else{
            System.out.println("공부하세요!");
        }


    }
}
